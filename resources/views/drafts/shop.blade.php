<h1 align="center">Butik och Ateljé</h1>

I anslutning till garveriet har jag min syateljé som även fungerar som butik. Aktuella öppettider finns på företagets facebook "Lottas Garfveri och Konsthanverk". Ring gärna i förväg om ni planerar ett besök eftersom jag undervisar och föreläser en hel del och därför inte alltid är där själv.
I ateljén säljer jag:
-mina böcker "Skinngarvning och beredning med traditionella metoder" samt
"Fiskskinn garvning och sömnad".
-hattar kepsar, skålar, väskor, gitarrband, dräkter och bälten
tillverkade av ren-, får-, älg-, fisk- och strutsskinn,
-garvade renskinn samt pälsberedda fårskinn.
-vikingainspirerade smycken i silver, järn och brons.
-koftor och tröjor i lin, bomull och ull.
Mycket av det jag tillverkar är beställningar från privatkunder och museér.

