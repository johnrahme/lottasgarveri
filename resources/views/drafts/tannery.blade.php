Bruket av fiskskinn har haft en lång tradition i samhällen längst älvar, floder och kuster
runt om i världen.
I artfattiga områden blev fiskskinn betraktat som ett nyttigt och användbart material.
Hos inuiter (eskimåer) i norra Kanada fick jag se stövlar med skaft av laxskinn och
sulor av sälskinn. Jag fick veta att tunna fiskskinn garvades med urin från en pojkbaby
som fortfarande ammas av sin mor, medan tjocka fiskskinn skulle garvas med urin från
en pojke i målbrottet. Jag blev intresserad och när jag kom hem började jag experimentera.
Idag garvar jag mina fiskskinn främst med bark eller med rapsolja.

